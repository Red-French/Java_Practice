package com.redfrench.house;

public class Kitchen {

	private String cabinetMaterial;
	private String counterTop;
	
	public Kitchen(String cabinetMaterial, String counterTop) {
		super();
		this.cabinetMaterial = cabinetMaterial;
		this.counterTop = counterTop;
	}

	public String getCabinetMaterial() {
		return cabinetMaterial;
	}

	public String getCounterTop() {
		return counterTop;
	}
	
	
		
}
